#include <iostream>
using namespace std;
int main(){
  int n;
  cout<<"hello give the length for the wave"<<endl;
  cin>>n;
 
for (int i = 1;; i++)
{
  for (int a = 1; a <= n; a++)
  {

    for (int j = 1; j <= a; j++)
    {
      if (j == a)
      {
        cout << '*';
      }
      else
      {
        cout << ' ';
      }
    }
    cout << endl;
  }

  for (int w = n; w >= 1; w--)
  {
    for (int e = 1; e <= w; e++)
    {
      if (e == w)
      {
        cout << '*';
      }
      else
      {
        cout << ' ';
      }
    }
    cout << endl;
  }
}
}
